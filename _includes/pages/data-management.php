<h2>Data Management</h2>

<p>Axiom Data Science designs, develops and supports systems for full-cycle scientific data management. This includes supporting data collection, sharing and preservation from the time that a sample is collected to when the laboratory analysis is submitted to a national archive center.</p>

<h3>Collaboration</h3> 

<p>Science is a team effort. Raw data, processing scripts, data collection logs, cruise reports, and initial results all need to be quickly, easily and securely shared among team members. Data synthesis can only be achieved when scientists are enabled to work together. Emailing powerpoint slides of graphs is not the answer.</p>

<h3>Safety and Security</h3>

<p>Data collection represents the culmination of years of planning and logistics. When it comes back from the field, you want to be able to share data with your collaborators, while simultaneously having it backed up (in triplicate) away from your personal hard drive.</p>

<h3>Documentation</h3>

<p>Metadata is critical to making data usable by future scientists. Unfortunately many researchers find creating standardized metadata onerous. We build tools that can help.</p>

<h3>Publication</h3>

<p>Ultimately, for data to be useful to the entire community, it needs to be published in an publicly available data archive. Data publication is also often a requirement of many funding opportunities. We'll streamline that process so you won't have to fill 12-pages of paperwork for every dataset.</p>

<h3>Who We Work With</h3>

<p>Axiom Data Science, through its partnership with the Research Workspace, provides data management services to multiple, large-scale research programs including the Gulf of Alaska Integrated Ecosystem Research Project, the Arctic Ecosystem Integrated Survey, the Russian American Long-term Census of the Arctic, GulfWatch Alaska, the Distributed Biological Observatory, and more.</p>
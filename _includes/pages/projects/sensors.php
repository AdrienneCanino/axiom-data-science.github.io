<h4>The Project</h4>

<p>Real-time sensor data visualizations harvest, aggregate and display information from many types of atmospheric and oceanographic measurement stations. Each station includes sensors that stream measurements of temperature, pressure, currents, tides, streams, snow,  and more. Some stations also include current streaming photos (i.e., webcams). Station measurements can be used to current conditions in a particular location, or to find overall trends across an entire region. When combined with other data layers such as forecasts, they can even be used for model validation.</p>

<h4>Challenges</h4>

<p>There are thousands of real-time sensors sprawling across North America, and each station includes multiple sensors. Sensor streams come from a variety of data providers, each with their own data processing and streaming standards.</p>

<h4>Solutions</h4>

<p>Putting thousands of disparate measurements into an informative visualization requires careful, continuous data harvesting and storage, standardization of units and datums and optimization of the web-application. Synoptic views of the data are achieved by using hex-binning and statistics at lower zoom levels, whereas zooming in shows raw data values from individual sensors. Our maps allows users to browse the map by sensor type, quickly determine the normality or abnormality of a particular sensor's current readings, find and download data from individual sensors, and connect to source data and metadata.</p>

<h4>Highlights</h4>

<ul>
<li>Hex binning with roll-over statistical reports.</li>
<li>Download of sensor measurements from individual sensors.</li>
</ul>





